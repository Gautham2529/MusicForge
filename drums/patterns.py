    # ---------------------------------------------------------

    def get(self, style="rock", section="verse"):

        if style not in self.styles:
            style = "rock"

        if section not in self.styles[style]:
            section = "verse"

        return random.choice(
            self.styles[style][section]
        )

    # ---------------------------------------------------------

    def styles_available(self):

        return list(self.styles.keys())

    # ---------------------------------------------------------

    def sections_available(self, style="rock"):

        if style not in self.styles:
            style = "rock"

        return list(self.styles[style].keys())